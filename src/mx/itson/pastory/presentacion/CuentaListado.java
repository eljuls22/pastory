        public void cargar() {
        DefaultTableModel modelo = (DefaultTableModel) tblCuenta.getModel();
        modelo.setRowCount(0);
        List<Cuenta> cuentas = CuentaDAO.obtenerPorId(this.id);
        for (Cuenta c : cuentas) {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getCliente().getId(),
                c.getNumero(),
                c.getCliente().getNombre(),
                c.getCliente().getEmail()
            });
        }
    }
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
      
              DefaultTableModel modelo = (DefaultTableModel) tblCuenta.getModel();
        int id = Integer.parseInt(String.valueOf(modelo.getValueAt(tblCuenta.getSelectedRow(), 0)));
        int confirmar = JOptionPane.showConfirmDialog(null, "Â¿Realmente desea eliminar este registro?", "Confirmar eliminacion", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (confirmar == 0) {
            CuentaDAO.Eliminar(id);
            cargar();
        } else {
            cargar();
        }  
        
    }//GEN-LAST:event_btnEliminarActionPerformed

